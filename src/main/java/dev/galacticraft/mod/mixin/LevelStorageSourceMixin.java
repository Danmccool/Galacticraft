package dev.galacticraft.mod.mixin;

import com.mojang.datafixers.DataFixer;
import net.minecraft.nbt.CompoundTag;
import net.minecraft.nbt.Tag;
import net.minecraft.world.level.storage.LevelStorageSource;
import net.minecraft.world.level.storage.LevelSummary;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfoReturnable;
import org.spongepowered.asm.mixin.injection.callback.LocalCapture;

import java.nio.file.Path;

@Mixin(LevelStorageSource.class)
public class LevelStorageSourceMixin {
    @Inject(method = "method_29015", at = @At(value = "INVOKE", target = "Lnet/minecraft/nbt/CompoundTag;getCompound(Ljava/lang/String;)Lnet/minecraft/nbt/CompoundTag;"), locals = LocalCapture.CAPTURE_FAILEXCEPTION)
    private void gatherOldFMLData(LevelStorageSource.LevelDirectory levelDirectory, boolean bl, Path path, DataFixer dataFixer, CallbackInfoReturnable<LevelSummary> cir, Tag tag) {
        CompoundTag levelData = (CompoundTag) tag;
        if (levelData.contains("FML")) {
            CompoundTag fmlData = ((CompoundTag)tag).getCompound("FML");
        }
    }
}
